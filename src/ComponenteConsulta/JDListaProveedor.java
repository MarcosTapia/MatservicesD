    public JDListaProveedor(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        try {
            UIManager.setLookAndFeel(UIManager.getSystemLookAndFeelClassName());
        } catch (Exception e) {
            e.printStackTrace();
        }
        configuracionBean = configuracionDAO.obtieneConfiguracion(1);
        this.setTitle(configuracionBean.getNombreEmpresa());
        String titulos[] = {"NIT", "NOMBRE", "DIRECCION","NroFAX", "TIPO TELEF.", "NUMERO TELF.", "ESTADO"};
        LProveedor.setColumnIdentifiers(titulos);
        try {
            for (ProveedorBean p : BDProveedor.mostrarProveedor()) {
                String Datos[] = {String.valueOf(p.getnPRovNit()), p.getcProvNombre(), p.getcProvDireccion(), p.getcProvNroFax(), p.getcProvTipoTelefono(), p.getcProvNumTelefono(), p.getcProvEstado()};
                LProveedor.addRow(Datos);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "ERROR: " + ex.getMessage());
        }
        initComponents();
    }

